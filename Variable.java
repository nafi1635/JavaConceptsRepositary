public class Variable
{
	public static void main(String args[])
	{
		int i=10;
		System.out.println("Data Type Integer : = "+i);
		char c='s';
		System.out.println("Data Type Char : = "+c);
		String s="smart nafi";
		System.out.println("Data Type String : = "+s);
		double d=16.0;
		System.out.println("Data Type Double : = "+d);
		long l=99;
		System.out.println("Data Type Long : = "+l);
		byte b=100;
		System.out.println("Data Type Byte : = "+b);
		float ss=7;
		System.out.println("Data Type Float : = "+ss);
		short sh=121;
		System.out.println("Data Type Double : = "+sh);
		
				
	}
}
